function cDroneSigns:CreateSigns()
    self.signs = {}

    for _, sign in pairs(self.sign_data) do
        self.signs[_] = StaticObject.Create({
            position = sign.pos,
            angle = sign.ang * Angle(math.pi, 0, 0),
            model = "53x17.nlz/go120_06-e2.lod",
            collision = "53x17.nlz/go120_06_lod1-e2_col.pfx"
        })
    end
end

function cDroneSigns:ModuleUnload()
    for _, object in pairs(self.signs) do
        object:Remove()
    end
end

